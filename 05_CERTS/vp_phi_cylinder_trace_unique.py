    print("STRUCTURE_FIXED_BEFORE_NUMBER: golden subshift transition M=[[1,1],[1,0]] (the 'no bb' rule), primitive; "
          "the trace is the normalized shift-invariant state on the finite cylinder tower, fixed before any value")

    M = [[1, 1], [1, 0]]
    M2 = [[sum(M[i][k] * M[k][j] for k in range(2)) for j in range(2)] for i in range(2)]
    assert all(M2[i][j] > 0 for i in range(2) for j in range(2)), "golden transition must be primitive (M^2 > 0)"
    print(f"PASS_PRIMITIVE_TRANSITION  M^2={M2} strictly positive => Perron-Frobenius gives a UNIQUE stationary state")

