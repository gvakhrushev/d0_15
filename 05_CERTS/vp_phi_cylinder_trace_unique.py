from __future__ import annotations

import sys
from fractions import Fraction as F

if hasattr(sys.stdout, "reconfigure"):
    sys.stdout.reconfigure(encoding="utf-8")

PHI = (1.0 + 5.0 ** 0.5) / 2.0


# --- exact ℚ(φ): (a,b) = a + b·φ, with φ² = φ + 1 ---
def mul(x, y):
    a, b = x
    c, d = y
    return (a * c + b * d, a * d + b * c + b * d)


def add(*xs):
    return (sum(x[0] for x in xs), sum(x[1] for x in xs))


def smul(c, x):
    return (c * x[0], c * x[1])


def val(x):
    return float(x[0]) + float(x[1]) * PHI


ONE = (F(1), F(0))
PHIv = (F(0), F(1))
PHI_INV = (F(-1), F(1))     # φ⁻¹ = φ − 1
PHI_INV2 = (F(2), F(-1))    # φ⁻² = 2 − φ


def left_apply(pi, M):
    """(π·M)_j = Σ_i π_i · M_ij, for 2×2 integer M and π a pair of ℚ(φ) values."""
    return tuple(add(*[smul(M[i][j], pi[i]) for i in range(2)]) for j in range(2))


