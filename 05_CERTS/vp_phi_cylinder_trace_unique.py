    assert piM == phi_pi, f"stationary trace must satisfy pi M = phi pi: {piM} vs {phi_pi}"
    assert add(pi[0], pi[1]) == ONE, f"trace must be normalized to 1: {add(pi[0], pi[1])}"
    print(f"PASS_UNIQUE_STATIONARY_TRACE  pi=(phi^-1,phi^-2)=({val(pi[0]):.6f},{val(pi[1]):.6f}); pi M = phi pi; sum=1 "
          "(the golden split W_ext/W_int = the unique cylinder frequencies, ties to D0-IM-002)")

