    final_dimension = dimensions[-1]

    wrong_dim2 = effective_dimension(sides[-1], us, dimension=2)
    terminal_only_sides = [8, 8, 8, 8]
    terminal_only_growth = is_strictly_increasing(terminal_only_sides)
    no_archive_growth = is_strictly_increasing([sides[0]] * len(sides))
    wrong_projection_surjective = constant_projection_is_surjective(
        source_size=sides[-1] ** 4,
        target_size=sides[0] ** 4,
    )
    wrong_phase_passes = non_phi_generator_guard(math.sqrt(2.0) - 1.0)

    # Required Negative Controls
    # 1. Pullback-only Laplacian (no metric refinement)
    def pullback_only_laplacian_eigenvalues(side: int) -> list[float]:
        fixed_side = 8
        scale = 4.0 * fixed_side * fixed_side
        base = [scale * (math.sin(math.pi * k / fixed_side) ** 2) for k in range(fixed_side)]
        return base + [0.0] * (side - fixed_side)

    def pullback_heat_trace(side: int, u: float, dimension: int = 4) -> float:
        one_dim = sum(math.exp(-u * lam) for lam in pullback_only_laplacian_eigenvalues(side))
        return one_dim ** dimension

    def pullback_effective_dimension(side: int, us: list[float], dimension: int = 4) -> float:
        traces = [pullback_heat_trace(side, u, dimension) for u in us]
        slope = fit_slope([math.log(u) for u in us], [math.log(t) for t in traces])
        return -2.0 * slope

    # 2. Non-local Laplacian (all-to-all)
    def nonlocal_laplacian_eigenvalues(side: int) -> list[float]:
        scale = 4.0 * side * side
        return [0.0] + [scale * side] * (side - 1)

    def nonlocal_heat_trace(side: int, u: float, dimension: int = 4) -> float:
        one_dim = sum(math.exp(-u * lam) for lam in nonlocal_laplacian_eigenvalues(side))
        return one_dim ** dimension

    def nonlocal_effective_dimension(side: int, us: list[float], dimension: int = 4) -> float:
        traces = [nonlocal_heat_trace(side, u, dimension) for u in us]
        slope = fit_slope([math.log(u) for u in us], [math.log(t) for t in traces])
        return -2.0 * slope

