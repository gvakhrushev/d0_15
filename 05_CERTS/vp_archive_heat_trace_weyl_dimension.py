#!/usr/bin/env python3
"""Archive heat-trace Weyl-dimension cert for the v12.8 archive tower.

The Lean archive tower gives `fibers(n) = n + 2` and `modes(n) = fibers(n)^4`.
This cert checks the concrete four-dimensional periodic archive Laplacian

    lambda(k_1,...,k_4) = sum_i 4 L^2 sin(pi k_i / L)^2

without materializing the L^4 matrix.  The heat trace factorizes as

    Theta_L(u) = (sum_k exp(-u lambda_1d(k)))^4.

This is a finite-object cert, not an Einstein-limit proof.
"""

from __future__ import annotations

import json
import math
import sys
from pathlib import Path

if hasattr(sys.stdout, "reconfigure"):
    sys.stdout.reconfigure(encoding="utf-8")


ROOT = Path(__file__).resolve().parents[1]
NO_GO = ROOT / "NO_GO_ARCHIVE_WEYL_DIMENSION.md"
STATUS = "PASS_ARCHIVE_HEAT_TRACE_WEYL_DIMENSION"


def archive_fibers(n: int) -> int:
    return n + 2


def archive_modes(n: int) -> int:
    return archive_fibers(n) ** 4


def one_dim_laplacian_eigenvalues(side: int) -> list[float]:
    scale = 4.0 * side * side
    return [scale * (math.sin(math.pi * k / side) ** 2) for k in range(side)]


def heat_trace(side: int, u: float, dimension: int = 4) -> float:
    one_dim = sum(math.exp(-u * lam) for lam in one_dim_laplacian_eigenvalues(side))
    return one_dim**dimension


def fit_slope(xs: list[float], ys: list[float]) -> float:
    mean_x = sum(xs) / len(xs)
    mean_y = sum(ys) / len(ys)
    denom = sum((x - mean_x) ** 2 for x in xs)
    if denom == 0.0:
        raise ValueError("degenerate x-axis for slope fit")
    return sum((x - mean_x) * (y - mean_y) for x, y in zip(xs, ys)) / denom


def effective_dimension(side: int, us: list[float], dimension: int = 4) -> float:
    traces = [heat_trace(side, u, dimension) for u in us]
    if any(t <= 0.0 or not math.isfinite(t) for t in traces):
        raise ValueError("non-positive or non-finite heat trace")
    slope = fit_slope([math.log(u) for u in us], [math.log(t) for t in traces])
    return -2.0 * slope


def is_strictly_increasing(xs: list[int]) -> bool:
    return all(a < b for a, b in zip(xs, xs[1:]))


def constant_projection_is_surjective(source_size: int, target_size: int) -> bool:
    if target_size <= 0:
        return False
    image = {0 for _ in range(source_size)}
    return len(image) == target_size


def non_phi_generator_guard(generator: float, tol: float = 1e-14) -> bool:
    phi = (1.0 + math.sqrt(5.0)) / 2.0
    d0_generator = phi ** -2
    return abs(generator - d0_generator) <= tol


def write_no_go(payload: dict[str, object]) -> None:
    NO_GO.write_text(
        "\n".join(
            [
                "# NO-GO: archive Weyl dimension cert",
                "",
                "Generated by `05_CERTS/vp_archive_heat_trace_weyl_dimension.py`.",
                "",
                "The finite archive heat-trace cert did not satisfy the declared",
                "four-dimensional Weyl scaling checks or its negative-control discipline.",
                "",
                "```json",
                json.dumps(payload, indent=2, sort_keys=True),
                "```",
                "",
            ]
        ),
        encoding="utf-8",
    )


def cleanup_stale_no_go() -> None:
    if not NO_GO.exists():
        return
    text = NO_GO.read_text(encoding="utf-8", errors="replace")
    if "Generated by `05_CERTS/vp_archive_heat_trace_weyl_dimension.py`." in text:
        NO_GO.unlink()


